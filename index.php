        3. den andel fakturor som har betalats efter utgången av avtalad betalningstid.<br><br>

            Uppgifterna ska redovisas uppdelat på storlekskategorierna<br>
        1. företag som har 0-9 anställda,<br>
        2. företag som har 10-49 anställda, och<br>
        3. företag som har 50-249 anställda.</p>
